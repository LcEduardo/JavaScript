// OPERADORES:
// Operadores aritméticos:
var n1 = 5;
var n2 = 6;

n1 * n2; //MULTIPLICAÇÃO.
n1 + n2; //ADIÇÃO.
n1 / n2; //DIVISÃO.
n1 % n2; //RESTO DA DIVISÃO.

//Incremento e decremento:
//Pré-incremento:
var x = 5;
var y = ++x;
console.log(x); //6
console.log(y); //6

//Pós-incremento:
var a = 10;
var b = a++;
console.log(a); //11
console.log(b); //10

//Decremento:
var z = 5;
var w = --z;

console.log(z); //4
console.log(w); //4

var l = 5;
var m = l--;

console.log(l); //4
console.log(m); //5
